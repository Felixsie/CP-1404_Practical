
#This for loop is a loop that displays all of the odd numbers between 1 and 20 with a space between each one.
for i in range(1, 21, 2):
 print(i, end=' ',)
print("\n")

#This for loop is a loop that displays count in 10s from 0 to 100
for i in range(0, 101, 10):
 print(i, end=' ')
print("\n")

#This for loop is a loop that displays count down from 20 to 1
for i in range(20, 0, -1):
 print(i, end=' ')
print("\n")